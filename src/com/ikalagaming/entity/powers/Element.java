package com.ikalagaming.entity.powers;

/**
 * An element describes the type of damage or resistance a given power may have.
 * Powers that are fire based, for example, would likely deal fire damage.
 *
 * @author Ches Burks
 *
 */
public enum Element {
	WATER,
	FIRE,
	STONE,
	PLANT,
	ELECTRIC,
	PSYCHIC,
	LIGHT,
	DARK,
	PHYSICS,
	MAGIC,
	ICE;
}
